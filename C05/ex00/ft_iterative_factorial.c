#include <stdio.h>

int	ft_iterative_factorial(int nb)
{
	int	result;
	int	i;

	i = 1;
	result = 1;
	if (nb < 0)
		return (0);
	while (i < nb + 1)
		result *= i++;
	return (result);
}

